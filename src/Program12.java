public class Program12 {

    public static void main(String args[])
    {
        int i = 1;
        while (i <= 100) {
            System.out.println("Printing while loop = " +i);
            i++;
        }
        for (i = 1; i <= 100; i++) {
            System.out.println("Printing for loop = " +i);
        }
    }
}
